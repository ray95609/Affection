package uuu.Affection.exception;

public class AffectionAtaInvalidException extends RuntimeException {

	public AffectionAtaInvalidException() {
		super();
		
	}

	public AffectionAtaInvalidException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public AffectionAtaInvalidException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public AffectionAtaInvalidException(String message) {
		super(message);
		
	}

	public AffectionAtaInvalidException(Throwable cause) {
		super(cause);
		
	}

}
