package uuu.Affection.exception;



public class AffectionException extends Exception{

	public AffectionException() {
		super();
		
	}

	public AffectionException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public AffectionException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public AffectionException(String message) {
		super(message);
		
	}

	public AffectionException(Throwable cause) {
		super(cause);
		
	}

	
	
}
